class Rectangle:
    def __init__(self, width, height, color):
        self.width = width
        self.height = height
        self.color = color

class Polygon:
    def __init__(self, points, color):
        self.points = points
        self.color = color

class Circle:
    def __init__(self, radius, color):
        self.radius = radius
        self.color = color